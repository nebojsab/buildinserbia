// Normalize a municipality string for lookup
export function normalizeMunicipalityKey(raw: string): string {
  return raw
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "") // strip diacritics
    .replace(/[čć]/g, "c")
    .replace(/[šđ]/g, (c) => c === "š" ? "s" : "dj")
    .replace(/ž/g, "z")
    .replace(/lj/g, "lj")
    .replace(/nj/g, "nj")
    .replace(/opština\s*/i, "")
    .replace(/grad\s*/i, "")
    .replace(/,.*$/, "") // strip everything after comma
    .trim();
}

export function findMunicipalityPortal(rawInput: string): { name: string; url: string } | null {
  const key = normalizeMunicipalityKey(rawInput);
  // Exact match first
  if (municipalityPortals[key]) return municipalityPortals[key];
  // Partial match (input contains or is contained by a key)
  for (const [k, v] of Object.entries(municipalityPortals)) {
    if (key.includes(k) || k.includes(key)) return v;
  }
  return null;
}
